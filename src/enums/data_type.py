from enum import Enum


class DataTypes(Enum):
    PHONE_NUMBER = [
        r"(?:(?:90|0|\+90)\s?)?(?:\d{3}[\d\-.\s]?){2}(?:\d{4}|(?:\d{2}[-.\r\t\f ]?\d{2}))"
    ]
    ID_NUMBER = [r"[1-9]\d{10}"]
    CREDIT_CARD_NUMBER = [
        r"(?<=(?:[.* ]))(?:\d{4}[-.\r\t\f \/]?\d{4}[-.\r\t\f \/]?\d{4}[-.\r\t\f \/]?\d{7})|"
        r"(?:(?:\d{4}[-.\r\t\f \/]?){4})|(?:\d{4}[-.\r\t\f \/]?\d{6}[-.\r\t\f \/]?\d{5})"
    ]
    PLATE = [
        r"(?<=[^\n])(?:\d{2}[-.\r\t\f ]?)(?:(?!.*[xXqQ])[a-zA-Z]{1,3}[-.\r\t\f ]?)(?:\d{2,5})"
    ]
    DATE = [
        r"(?<=(?:[.* ]))(?:(?:[a-zA-Z]{3,}[-.\r\t\f\/ ]?(?:\d{1,4}[-.\r\t\f\/ ]?){2})|"
        r"(?:\d{1,4}[-.\r\t\f\/ ]?[a-zA-Z]{3,}[-.\r\t\f\/ ]?\d{1,4}[-.\r\t\f\/ ]?)|"
        r"(?:(?:\d{1,4}[-.\r\t\f\/ ]?){2}[a-zA-Z]{3,}[-.\r\t\f\/ ]?)|(?:(?:\d{1,4}[-.\r\t\f\/ ]+){2}\d{1,4}))"
    ]
    EMAIL = [r"[\w\-\.]+@(?:[\w-]+\.)+[\w-]{2,4}"]
    DOMAIN = [r"(?<=(?:[.* ]))(?!.*@)(?:[a-zA-Z0-9\-])+(?:\.[a-zA-Z0-9\-]+)+"]
    URL = [
        r"(?<=\s)(?!.*@)(?:(?:http[s]?|ftp):\/\/)?(?:www\.)?(?:(?:[^\s:\/\\]+\.)+(?:\w{2,})(?:\/\S*)*)"
    ]
    HASH = [r"^[0-9a-f]{32}$", r"^[0-9a-f]{40}$", r"^[0-9a-f]{64}$"]
    COMBOLIST = [r"(?:\S+)\s*\:\s*(?:\S+)"]
